// Comparison Operator
// Task 1: Write a program to compare two numbers using > and < and log the result to the console.

// Task 2: Write a program to compare two numbers using >= and <= and log the result to the console.

// Task 3: Write a program to compare two numbers using == and === and log the result to the console.




// Solution
// Task 1:
const numOne = 20
const numTwo = 30
const result = numOne > numTwo
const resultTwo = numOne < numTwo
console.log(result)
console.log(resultTwo)


// Task 2: 
const resultThree = numOne >= numTwo
const resultFour = numOne <= numTwo
console.log(resultThree)
console.log(resultFour)



// Task 3: 
const resultFive = numOne == numTwo
const resultSix = numOne === numTwo
console.log(resultFive)
console.log(resultSix)